#define RAI_T(kc) LT(_RAISE, kc)
#define LOW_T(kc) LT(_LOWER, kc)
#define DEA_T(kc) LT(_DEAD, kc)

enum layers {
 _QWERTZ,
 _GAMING,
 _DVORAK,
 _RAISE,
 _LOWER,
 _MOUSE,
 _DEAD
};

enum custom_keycodes {
  QWERTZ = SAFE_RANGE,
  GAMING,
  DVORAK
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
    case QWERTZ:
        if (record->event.pressed) {
            set_single_persistent_default_layer(_QWERTZ);
        }
        return false;
        break;

    case GAMING:
        if (record->event.pressed) {
            set_single_persistent_default_layer(_GAMING);
        }
        return false;
        break;

    case DVORAK:
        if (record->event.pressed) {
            set_single_persistent_default_layer(_DVORAK);
        }
        return false;
        break;
    }
    return true;
};

